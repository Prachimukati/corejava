package in.com.inheritance;

public class School {

	private String name;
	private String student;
	
	public void setname(String name) {
		this.name = name;
	}
	public String getname() {
		return name;
	}
	public void SetStudent(String student) {
		this.student = student;
	}
	public String getStudent() {
		return student;
	}
}
