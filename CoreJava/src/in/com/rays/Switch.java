package in.com.rays;

public class Switch {
public static void main(String[] args) {
	int fruits = 7;
	switch(fruits) 
	{
	case 1:
		System.out.println("Apple");
		break;
	case 2:
		System.out.println("Mango");
		break;
	case 3:
		System.out.println("Grapes");
		break;
	case 4:
		System.out.println("Orange");
		break;
	case 5:
		System.out.println("Banana");
		break;
   default:
	   System.out.println("default");
	   break;
	}
			
}
}
