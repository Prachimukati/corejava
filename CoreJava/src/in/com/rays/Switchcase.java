package in.com.rays;

public class Switchcase {
public static void main(String[] args) {
	int dayofweek = 3;
	switch(dayofweek)
	{
	case 1:
		System.out.println("sunday");
		break;
	case 2:
		System.out.println("monday");
		break;
	case 3:
		System.out.println("tuesday");
		break;
   default:
	   System.out.println("default part");
	   break;
	}
	
}
}
