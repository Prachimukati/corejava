package in.com.Strbuffmathtypetypecasting;

public class Math {
public static void main(String[] args) {
	
	System.out.println("max 100,10 ="+ Math.max(100, 10));
	System.out.println("exp 100 =" + Math.exp(100));
	System.out.println("random  =" + Math.random());
	System.out.println("sqrt 5  =" + Math.sqrt(5));
	System.out.println("min 100,10  ="+ Math.min(100, 10));
	System.out.println("abs 5.2 =" + Math.abs(5.2));
	
}
}
